document.addEventListener('DOMContentLoaded', () => {
    renderNav('problems');

    if (typeof requireAuth === 'function') {
        if (!requireAuth()) return;
    }

    const container = document.getElementById('problemsList');
    const problems = parseRoadmap(ROADMAP_SOURCE);
    renderProblems(container, problems);
});

function parseRoadmap(input) {
    const lines = input
        .split('\n')
        .map(line => line.trim())
        .filter(Boolean);

    const problems = [];
    let section = 'General';

    for (const line of lines) {
        const numbered = line.match(/^(\d+)\s+(.+)$/);
        if (numbered) {
            const leetcodeNo = Number(numbered[1]);
            const title = numbered[2].trim();
            problems.push({ leetcodeNo, title, topic: section });
            continue;
        }

        const parsedTopic = extractTopicName(line);
        if (parsedTopic) {
            section = parsedTopic;
            continue;
        }
    }

    return problems.map((problem, index) => ({
        sNo: index + 1,
        ...problem,
        url: buildLeetcodeUrl(problem.leetcodeNo, problem.title)
    }));
}

function extractTopicName(line) {
    // Match topic lines like '1️⃣ Prefix Sum (30)' and extract only the topic name (no symbol, no count)
    const topicMatch = line.match(/^[^a-zA-Z0-9]*\s*([a-zA-Z\s]+).*$/);
    if (!topicMatch) return null;
    return topicMatch[1].trim();
}

function buildLeetcodeUrl(leetcodeNo, title) {
    const isVariant = /variant|revisit/i.test(title);
    if (isVariant) {
        return `https://leetcode.com/problemset/all/?search=${encodeURIComponent(String(leetcodeNo))}`;
    }

    const slug = title
        .toLowerCase()
        .replace(/&/g, ' and ')
        .replace(/\+/g, ' plus ')
        .replace(/[^a-z0-9\s-]/g, '')
        .replace(/\s+/g, '-')
        .replace(/-+/g, '-')
        .replace(/^-|-$/g, '');

    return `https://leetcode.com/problems/${slug}/description/`;
}

function renderProblems(container, problems) {
    if (!problems.length) {
        container.innerHTML = '<div class="empty-state"><p>No roadmap items found.</p></div>';
        return;
    }

    let currentTopic = '';
    const bodyRows = problems.map(problem => {
        const topicHeadingRow = problem.topic !== currentTopic
            ? `<tr class="topic-heading-row"><td colspan="4">${escapeHtml(problem.topic)}</td></tr>`
            : '';

        currentTopic = problem.topic;

        return `
            ${topicHeadingRow}
            <tr>
                <td>${problem.sNo}</td>
                <td><span class="badge badge-accent">${problem.leetcodeNo}</span></td>
                <td><strong>${escapeHtml(problem.title)}</strong></td>
                <td>
                    <a class="btn btn-sm btn-primary" href="${problem.url}" target="_blank" rel="noopener noreferrer">
                        Solve
                    </a>
                </td>
            </tr>
        `;
    }).join('');

    container.innerHTML = `
        <div class="table-container">
            <table class="problems-master-table">
                <thead>
                    <tr>
                        <th style="width:72px;">S.No</th>
                        <th style="width:100px;">LC #</th>
                        <th>Problem</th>
                        <th style="width:130px;">Action</th>
                    </tr>
                </thead>
                <tbody>
                    ${bodyRows}
                </tbody>
            </table>
        </div>
    `;
}

function escapeHtml(value) {
    return String(value)
        .replaceAll('&', '&amp;')
        .replaceAll('<', '&lt;')
        .replaceAll('>', '&gt;')
        .replaceAll('"', '&quot;')
        .replaceAll("'", '&#39;');
}
